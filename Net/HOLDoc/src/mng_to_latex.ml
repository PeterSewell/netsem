(* mng_to_latex.ml -- turn .mng file into LaTeX code (with embedded typeset HOL) *)
(* Keith Wansbrough 2001-2004 *)

open Mngdump
open Holparse
open Holparsesupp
open Holparsetools

(* -------------------------------------------------------------------- *)
(*  Main program                                                        *)
(* -------------------------------------------------------------------- *)

let _ =
  let mngdoc = parse_fileargs ModeTex tex_main in
  (* NB: no trailing newline - we hope the first line of source is a comment already! *)
  print_string "%%%% AUTOGENERATED FILE (from MNG source) -- DO NOT EDIT! %%%% ";
  try
    rendertexdoc mngdoc;
    dump_unseen_strings ()
  with
    BadArg e ->
      prerr_endline (format_badarg e)

